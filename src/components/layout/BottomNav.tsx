import { useLocation, useRoute } from 'wouter'

const tabs = [
  { path: '/', label: 'Home', icon: HomeIcon },
  { path: '/game', label: 'Game', icon: GameIcon },
  { path: '/leaderboard', label: 'Board', icon: TrophyIcon },
  { path: '/history', label: 'History', icon: ClockIcon },
]

export function BottomNav() {
  const [location, setLocation] = useLocation()
  const [isSettlement] = useRoute('/game/settle')

  // Hide nav during settlement
  if (isSettlement) return null

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-slate-900/95 backdrop-blur border-t border-slate-800 safe-bottom z-40">
      <div className="flex justify-around max-w-lg mx-auto">
        {tabs.map((tab) => {
          const isActive =
            tab.path === '/'
              ? location === '/'
              : location.startsWith(tab.path)

          return (
            <button
              key={tab.path}
              onClick={() => setLocation(tab.path)}
              className={`
                flex flex-col items-center py-2 px-4 min-w-[64px] transition-colors
                ${isActive ? 'text-emerald-400' : 'text-slate-500 hover:text-slate-300'}
              `}
            >
              <tab.icon className="w-6 h-6" />
              <span className="text-xs mt-0.5 font-medium">{tab.label}</span>
            </button>
          )
        })}
      </div>
    </nav>
  )
}

